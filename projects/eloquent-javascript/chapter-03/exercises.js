////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {

return num1 < num2 ? num1 : num2;


}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(number) {
if(number === 0){
  return true;
  }else if(number === 1){
    return false;
  }else if(number < 0){
    return isEven(-number)
  }else{
    return isEven(number - 2);
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
let i;
let count = 0;
const passedString = string.split('');
const char2Check = char.toLowerCase();

for(i = 0; i < passedString.length; i++ ){
  if(passedString[i].toLowerCase() === char2Check){
    
    count++
    
    }
  
  }
return count;

}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
let i;
let count = 0;
const passedString = string.split('');
for(i = 0; i < passedString.length; i++){
  if(passedString[i].toUpperCase() === 'B'){
    
    count ++;
    
    }
  }

return count;

}

